(let [this-ns *ns*]
  (defn all-books []
    (->> (ns-publics this-ns)
         (map val)
         (map deref)
         (filter map?))))

(defmacro defbook
  [initials
   full-name
   principle
   difficulty
   period
   price
   subject
   memory]
  (let [expanded# {:name full-name
                   :mystery [(keyword principle) difficulty]
                   :period (keyword period)
                   :price price
                   :subject subject
                   :memory memory}]
    `(def ~initials ~expanded#)))
